import UIKit


//extiendo UIkit para poner unos colores para usarlos en la app

extension UIColor {
    
     class func defaultColorHacker() -> UIColor {
        return UIColor(red: 15.0/255.0, green: 134.0/255.0, blue: 11.0/255.0, alpha: 1.0);
    }

    
    
}